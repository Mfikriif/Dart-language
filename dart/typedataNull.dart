//Kata kunci null merupakan tipe data representasi data kosong.
// Secara default, saat kita ingin membuat variabel, data harus diisi, jika tidak maka variabel tidak akan bisa digunakan 
// Namun jika kita ingin sebuah variabel bisa diisi data null, kita bisa menambahkan karaakter "?", seperti: 
// TipeData?NamaVariabel = null;//

void main(){


int number1;
number1 = 10;
//number1 = null; //null tidak bisa di assigned kedalam variabel bertipe data integer
int? number2;

print(number1);
print(number2);

}

