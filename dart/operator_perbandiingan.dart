// == , sama dengan
// != , tidak sama dengan
// >, <, lebih dari dan kurang dari
// >= , <=, lebih dari sama dengan dan kurang dari sama dengan

void main() {
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print('eko' == 'eko');
  print('eko' != 'eko');
}

